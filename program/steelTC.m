function a=steelTC(m, t)

eps0=0.01; %—В–Њ—З–љ–Њ—Б—В—М

dd0=[100 200 300 400 500];

% 22–Ъ
% 12–Ь–•
% 15–Ь–•
% 12–•1–Ь–§
% 10–У–Э2–Ь–§–Р
% 08–•18–Э10–Ґ

switch m
    case "22–Ъ" % 22–Ъ
        aa0=[49.5 47.7 45.5 43.5 39.3];
    case "12–Ь–•" % 12–Ь–•
        aa0=[50.2 50.2 50.2 48.6 47];
    case "15–Ь–•" % 15MX
        aa0=[44.5 41.3 40.8 39 36.1];
    case "12–•1–Ь–§" % 12–•1–Ь–§
        aa0=[41.3 40.8 40.3 39.7 39];
    case "10–У–Э2–Ь–§–Р" % 10–У–Э2–Ь–§–Р
        aa0=[36 40 43 44 NaN];
    case "08–•18–Э10–Ґ" % 08–•18–Э10–Ґ
        aa0=[16.3 17.5 18.8 21.4 23];
end

%–Є–љ—В–µ—А–њ–Њ–ї—П—Ж–Є—П
dd1=linspace(100,500, 10001);
aa1=interp1(dd0, aa0, dd1, 'makima');

%–њ–Њ–Є—Б–Ї –љ—Г–ґ–љ–Њ–≥–Њ –Ј–љ–∞—З–µ–љ–Є—П
id = min(find(abs(dd1 - t) <= eps0));
a=aa1(id);
end

